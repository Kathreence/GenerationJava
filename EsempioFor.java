public class EsempioFor {
	public static void main (String[]args) { // args sarebbero le variabili?
		for (int i=0;i<5;i++) {
			System.out.println(i);
		}
			System.out.println("\n");
		for (int i=3;i<5;i++) {
			System.out.println(i);
		}
			System.out.println("\n");
		for (int i=0;i<3;i++) {
			System.out.println(i);
		}
			System.out.println("\n");
		for (int i=0;i<5;i+=2) {
			System.out.println(i);
		}
	}
}