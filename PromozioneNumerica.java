public class PromozioneNumerica{
	public static void main(String[] args){
		int a = 15;
		long b = 16;
		long c= a+b;
		System.out.println(c);

		float d = 12.3f;
		float e = a + d;
		System.out.println(e);

		byte b1 = 127;
		byte b2 = 1;
		byte b3 = (byte)(b1 + b2);
		System.out.println(b3);

		int b4 = b2 + b1;
		System.out.println(b4);
	}
}